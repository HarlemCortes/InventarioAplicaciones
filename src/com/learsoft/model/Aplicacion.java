package com.learsoft.model;

public class Aplicacion {

	int id;
	String nombreGeneral;
	String itemInventario;
	String itemItam;
	String descripcion;
	String nombreSvn;
	String nombreApps;
	String contenedorSeguridad;
	String contenedorItam;
	String svnUltimaVersion;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getNombreGeneral() {
		return nombreGeneral;
	}

	public void setNombreGeneral(String nombreGeneral) {
		this.nombreGeneral = nombreGeneral;
	}

	public String getItemInventario() {
		return itemInventario;
	}

	public void setItemInventario(String itemInventario) {
		this.itemInventario = itemInventario;
	}

	public String getItemItam() {
		return itemItam;
	}

	public void setItemItam(String itemItam) {
		this.itemItam = itemItam;
	}

	public String getDescripcion() {
		return descripcion;
	}

	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}

	public String getNombreSvn() {
		return nombreSvn;
	}

	public void setNombreSvn(String nombreSvn) {
		this.nombreSvn = nombreSvn;
	}

	public String getNombreApps() {
		return nombreApps;
	}

	public void setNombreApps(String nombreApps) {
		this.nombreApps = nombreApps;
	}

	public String getContenedorSeguridad() {
		return contenedorSeguridad;
	}

	public void setContenedorSeguridad(String contenedorSeguridad) {
		this.contenedorSeguridad = contenedorSeguridad;
	}

	public String getContenedorItam() {
		return contenedorItam;
	}

	public void setContenedorItam(String contenedorItam) {
		this.contenedorItam = contenedorItam;
	}

	public String getSvnUltimaVersion() {
		return svnUltimaVersion;
	}

	public void setSvnUltimaVersion(String svnUltimaVersion) {
		this.svnUltimaVersion = svnUltimaVersion;
	}

}
